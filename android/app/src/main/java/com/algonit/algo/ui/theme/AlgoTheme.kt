/**
 * Root theme composable for the Algo application.
 *
 * @param themeMode The user-selected theme mode (SYSTEM, LIGHT, or DARK).
 *                  When SYSTEM, follows the device dark theme setting.
 * @param dynamicColor Whether to use Android 12+ dynamic color (Material You).
 * @param content The composable content to render within the theme.
 */
@Composable
fun AlgoTheme(
    themeMode: ThemeMode = ThemeMode.SYSTEM,
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit
) {
    val darkTheme = when (themeMode) {
        ThemeMode.DARK -> true
        ThemeMode.LIGHT -> false
        ThemeMode.SYSTEM -> isSystemInDarkTheme()
    }

    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context)
            else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = AlgoTypography,
        shapes = AlgoShapes,
        content = content
    )
}
